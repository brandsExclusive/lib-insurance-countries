var getNames = function () {
  return countries.map(function (country) { return country.name })
}

var getCodes = function () {
  return countries.map(function (country) { return country.code })
}

var findByCode = function (code) {
  return countries.find(function (country) {
    return country.code.toLowerCase() === code.toLowerCase()
  })
}

var findByName = function (name) {
  return countries.find(function (country) {
    return country.name.toLowerCase() === name.toLowerCase()
  })
}

var riskMap = {
  worldwide: 1,
  europe: 2,
  asia: 3,
  pacific: 4,
  "new zealand": 5,
  domestic: 6,
}

var getHighestRiskRegion = function (destinationCodes) {
  var highRiskCountry = null
  var risk = null

  if (!destinationCodes || !destinationCodes.length) {
    throw new Error('Destinations are required')
  }

  destinationCodes.forEach(function (destinationCode) {
    var destination = findByCode(destinationCode)

    if (!destination) {
      throw new Error('Destination ' + destinationCode + ' could not be found')
    }

    var destinationRegion = destination.region.toLowerCase()

    if (!highRiskCountry || riskMap[destinationRegion] < risk) {
      risk = riskMap[destinationRegion]
      highRiskCountry = destinationCode
    }
  })

  return highRiskCountry
}

module.exports = {
  countries: countries,
  getNames: getNames,
  getCodes: getCodes,
  findByCode: findByCode,
  findByName: findByName,
  getHighestRiskRegion: getHighestRiskRegion,
  riskMap: riskMap
}
